/**
 * Get words by HSK level
 */
export function getWordsByLevel(level: 1 | 2 | 3): HSKWord[] {
  return HSK_WORDS.filter(word => word.level === level);
}

/**
 * Get a random word from a specific level
 */
export function getRandomWord(level: 1 | 2 | 3): HSKWord {
  const words = getWordsByLevel(level);
  return words[Math.floor(Math.random() * words.length)];
}

/**
 * Get random words (unique)
 */
export function getRandomWords(level: 1 | 2 | 3, count: number): HSKWord[] {
  const words = getWordsByLevel(level);
  const shuffled = [...words].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, words.length));
}
